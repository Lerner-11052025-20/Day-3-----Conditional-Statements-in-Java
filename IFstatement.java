public class IFstatement
{
    public static void main(String args[])
    {
        int a = 20;
        
        if(a >= 18)
        {
            System.out.println("Adult");
        }
        else
        {
            System.out.println("Not an Adult");
        }

        if(a < 18)
        {
            System.out.println("Not an Adult");
        }

    }
}
